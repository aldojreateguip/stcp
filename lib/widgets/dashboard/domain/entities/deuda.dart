class Deuda {
  String idDeuda;
  String anio;
  String cuota;
  String descripcion;
  String fase;
  String fechaVence;
  String insoluto;
  String interes;
  String gastos;
  String total;

  Deuda({
    required this.idDeuda,
    required this.anio,
    required this.cuota,
    required this.descripcion,
    required this.fase,
    required this.fechaVence,
    required this.insoluto,
    required this.interes,
    required this.gastos,
    required this.total,
  });
}
